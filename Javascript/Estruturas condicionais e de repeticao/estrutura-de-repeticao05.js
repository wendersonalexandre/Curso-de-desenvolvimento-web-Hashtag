// DO while

let numero = 1;

do {
    console.log("Numero: " + numero);
    numero++;
} while( numero <= 5);

console.log("Contegem do.. while foi concluida!!");

let funcionariosParaRegritrar = 6;

do {
    console.log("Registrando a entrada do funcionario " + funcionariosParaRegritrar);
    funcionariosParaRegritrar--;
} while(funcionariosParaRegritrar > 0);

console.log("Nenhum funcionario restantes para registrar");


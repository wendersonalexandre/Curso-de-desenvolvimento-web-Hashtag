// while

let numero = 0;

while(numero <= 5){
    console.log(numero);
    numero++
}

console.log("Contagem com while concluida");

let funcionariosRegistrados = 0;

while(funcionariosRegistrados < 3){
    console.log("Funcionario " + (funcionariosRegistrados + 1) + " registrou suas horas.");
    funcionariosRegistrados++;
}

console.log("Fim de registro");